/*************
Comparison Operators
*************/

// Equal
'3' == 3;

// Strictly Equals  -- mostly common 
3 === 3;

// Not Equal    -- "!" are also referred to as "bangs"
'3' != 4;

// Strictly Not Equal
'3' !== 3;

// Greater Than
3 > 2;

// Less Than
2 < 3;

// Greater Than or Equal To
3 >= 2;

// Less Than or Equal to
2 <= 3;

// And
2 && 3;

// Or
2 || 3;